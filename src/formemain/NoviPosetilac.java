    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            if (txtIme.getText().isEmpty() || txtKontakt.getText().isEmpty() || txtDatum.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sva polja su obavezna!");
                return;
            }
            
            SimpleDateFormat sdf=new SimpleDateFormat("dd.MM.yyyy");
            
            Posetilac p = new Posetilac(-1, txtIme.getText(), sdf.parse(txtDatum.getText()), txtKontakt.getText(), (Mesto) cmbMesto.getSelectedItem(), sesija.Sesija.getInstance().getUlogovani());
            
            KlijentKontroler.getInstance().addPosetilac(p);
            
            JOptionPane.showMessageDialog(this,"Sistem je zapamtio posetioca");
            this.dispose();
        
        } catch (ParseException ex) {
            Logger.getLogger(NoviPosetilac.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,"Sistem ne moze da zapamtio posetioca");
        }
        
        

    }//GEN-LAST:event_jButton1ActionPerformed

