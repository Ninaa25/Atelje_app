    private void popuni() {
        try {
            
            ArrayList<Mesto> mesta = KlijentKontroler.getInstance().getAllMesto();
            cmbMesto.removeAllItems();

            for (Mesto mesto : mesta) {
                cmbMesto.addItem(mesto);
            }
            cmbMesto.setSelectedIndex(-1);

        } catch (Exception ex) {
            Logger.getLogger(NoviPosetilac.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
