    private void srediFormu() {
        try {
            txtLokacija.setText(a.getLokacija());
            txtNaziv.setText(a.getNazivAteljea());
            popuni();
            cmbUmetnik.getModel().setSelectedItem(a.getUmetnik());
            ArrayList<Termin> termin_ateljea = KlijentKontroler.getInstance().getAllTerminAteljea(a);
            tblTermini.setModel(new ModelTabeleTermini(termin_ateljea));
        } catch (Exception ex) {
            Logger.getLogger(IzmenaAteljea.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    void dodaj(Termin t) {
        ModelTabeleTermini mt = (ModelTabeleTermini) tblTermini.getModel();
        mt.dodaj(t);
    }

    boolean postoji(Termin t) {
        ModelTabeleTermini mt = (ModelTabeleTermini) tblTermini.getModel();
        return mt.postoji(t);
    }

    private void popuni() {
        try {
            ArrayList<Umetnik> lista = KlijentKontroler.getInstance().getAllUmentik();
            cmbUmetnik.removeAllItems();
            for (Umetnik umetnik : lista) {
                cmbUmetnik.addItem(umetnik);
            }
        } catch (Exception ex) {
            Logger.getLogger(IzmenaAteljea.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void ocisti() {
        tblTermini.setModel(new ModelTabeleTermini());
        txtLokacija.setText(null);
        txtNaziv.setText(null);
    }
