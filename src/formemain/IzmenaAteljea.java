    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            if (txtLokacija.getText().isEmpty() || txtNaziv.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sva polja su obavezna!");
                return;
            }

            ModelTabeleTermini mt = (ModelTabeleTermini) tblTermini.getModel();
            Umetnik u = (Umetnik) cmbUmetnik.getSelectedItem();

            if (mt.getLista().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Atelje mora imati barem jedan definisan termin!");
                return;
            }

            a.setLista(mt.getLista());
            a.setLokacija(txtLokacija.getText());
            a.setNazivAteljea(txtNaziv.getText());
            a.setUmetnik(u);
            a.setRadnik(sesija.Sesija.getInstance().getUlogovani());

            KlijentKontroler.getInstance().updateAtelje(a);
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio atelje");
            PretragaAteljea p = (PretragaAteljea) getParent();
            p.osvezi();
            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem nije zapamtio atelje");
            
            //Logger.getLogger(IzmenaAteljea.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

